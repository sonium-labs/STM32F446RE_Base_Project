// Preprocessor directives
// "#include" essentially pastes the included file here
// .c files (like the one we're in) are called "source files"
// .h files (like those included) are called "header files"
// Really only header files are ever included
#include "main.h"

// We have to DECLARE functions in C before they are used (this is called a function declaration)
// Because we have the declaration here, we can call SystemClock_Config() and MX_GPIO_Init() in main()
// The DEFINITION is also in this file (this is called a function definition) below main
// You can only call functions BELOW where they are declared
void SystemClock_Config(void);
void GPIO_Init(void);

/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
    /* STM32F4xx HAL library initialization:
        - Configure the Flash prefetch, instruction and Data caches
        - Configure the Systick to generate an interrupt each 1 msec
        - Set NVIC Group Priority to 4
        - Global MSP (MCU Support Package) initialization
        */
    HAL_Init();

    // Autogenerated (by CubeMX) function that sets system clock speeds, turns on peripheral clock
    // Without this, we could not talk to any peripherals (cause clocks are needed to get data in or out of them)
    SystemClock_Config();

    // Turn on GPIO clocks
    // Without clocks on, we could not use any pins on the board, including those used for UART
    GPIO_Init();

    /*##-1- Configure the UART peripheral ######################################*/
    /* Put the USART peripheral in the Asynchronous mode (UART Mode) */
    /* UART1 configured as follows:
        - Word Length = 8 Bits
        - Stop Bit = One Stop bit
        - Parity = ODD parity
        - BaudRate = 9600 baud
        - Hardware flow control disabled (RTS and CTS signals) */

    /* UART handler declaration and definition */
    UART_HandleTypeDef uartHandle = {0};
    uartHandle.Instance          = USART2;
    uartHandle.Init.BaudRate     = 9600;
    uartHandle.Init.WordLength   = UART_WORDLENGTH_8B;  
    uartHandle.Init.StopBits     = UART_STOPBITS_1;
    uartHandle.Init.Parity       = UART_PARITY_NONE;    // STM32 Quirk: If you want to use a parity bit, the word length must be 9
    uartHandle.Init.HwFlowCtl    = UART_HWCONTROL_NONE;
    uartHandle.Init.Mode         = UART_MODE_TX_RX;
    uartHandle.Init.OverSampling = UART_OVERSAMPLING_16;
    if (HAL_UART_Init(&uartHandle) != HAL_OK)   // Note this calls HAL_UART_MspInit() in \Core\Src\stm32f4xx_hal_msp.c
    {                                           // which configures the correct GPIO to alternative mode.
        Error_Handler();                        // for them, the alt mode is USART2!
    }

    char buf[8] = "MIKU\n";

    while (1)   
    { 
        HAL_UART_Transmit(&uartHandle, buf, sizeof(buf), HAL_MAX_DELAY);
        HAL_Delay(1000);
    }
}

