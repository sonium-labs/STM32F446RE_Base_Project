/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void)
{
    // We do our initializations in this section, before we enter our endless loop (the while(1) at the bottom of main)

    // Autogenerated (by CubeMX) function that sets system clock speeds, turns on peripheral clock
    // Without this, we could not talk to any peripherals (cause clocks are needed to get data in or out of them)
    SystemClock_Config();

    // Autogenerated (by CubeMX) function that turns on GPIO peripheral clocks for Ports A, B, C, and H
    // B and H are maybe vestigial/unneeded, CubeMX had some reason to turn on those clocks tho
    // If we don't turn on the clocks, you can't talk to the GPIO Port
    MX_GPIO_Init();

    // GPIOC Pin 13 Setup //
    // PC13 is wired up externally to the user button (B1) on the Nucleo board
    // It's already configured!!!! cause the default config for a pin is:
    // - Mode (register MODER): Input
    // - Pull-Up/Pull-Down (register PUPDR): No Pull-Up/Pull-Down
    // ...which are both defined as 0b0/0b00, AND the default for those registers per the reference manual is all 0s,
    // so it's already set as input and no pull up/-down by default

    // GPIOA Pin 5 Setup //
    // PA5 is wired up externally to the user LED (LD2) on the Nucleo board
    GPIOA->ODR = 0x00000000; // Clear output data register as precaution
    GPIOA->MODER |= 0x400;   // Set MODER5[1:0] to 0b01 (General purpose output mode)
    // Don't need to worry about pull-up/-down, output devices don't care so that setting would be ignored!

    while (1)   // 1 is true, so while(1) is an endless loop
    { 
        // CMSIS SOLUTION
        // If button pressed
        if (GPIOC->IDR & GPIO_PIN_13)   // Bitwise AND (&)
        {   
            // Turn LED on (ACTIVE_LOW)
            // By "active low," I mean that "the device (LED) is active (LED turned on) when the logic level is low"
            // Logic level high is 1, logic level low is 0
            // Logic level high is 3.3V, logic level low is 0V
            // So to actually turn the LED on, we have to clear the bit!
            GPIOA->ODR &= ~(GPIO_PIN_5); // Set PA5 ("set" means set to 1); ~ is complement/bitwise NOT operator
        }
        else
        {
            // Turn LED off
            GPIOA->ODR |= (GPIO_PIN_5); // Clear PA5 ("clear" means set to 0); Bitwise OR (|)
        }
        // More about if statements: https://www.w3schools.com/c/c_conditions.php
        // More about else statements: https://www.w3schools.com/c/c_conditions_else.php
        // More about while: https://www.w3schools.com/c/c_while_loop.php 
        // More about C operators: https://www.w3schools.com/c/c_operators.php
    }
}

